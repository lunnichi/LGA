#include <stdlib.h>
#include "lga.h"

// Таблица столкновений:
uint8_t col_table[2][128]; // Двумерный массив: [0] - для газа, [1] - для препятствий

void init_collision_table() // Функция алгоритмической генерации правил столкновений
{
    // Физические векторы скоростей для всех 7 направлений.
    // Умножаем их на константы, чтобы избежать дробей и использовать только целые числа:
    int px[7] = {2,  1, -1, -2, -1,  1, 0}; // Проекции векторов на ось X (для 6 направлений + 7-я покоящаяся)
    int py[7] = {0, -2, -2,  0,  2,  2, 0}; // Проекции векторов на ось Y

    for (int i = 0; i < 128; i++) // Перебираем все возможные комбинации битов в байте (от 0 до 127)
    {
        // Считаем массу и импульс для текущего состояния клетки:
        int mass = 0, mx = 0, my = 0; // Инициализируем массу и проекции импульса нулями
        for (int b = 0; b < 7; b++) { // Перебираем 7 битов (направлений)
            if (i & (1 << b)) {       // Если бит под номером 'b' равен 1 (частица есть)
                mass++;               // Увеличиваем общую массу клетки на 1
                mx += px[b];          // Прибавляем вектор скорости частицы к общему импульсу по X
                my += py[b];          // Прибавляем вектор скорости частицы к общему импульсу по Y
            }
        }

        // Ищем все возможные состояния с точно такой же физикой:
        int valid_states[128]; // Массив для хранения подходящих исходов (куда могут разлететься частицы)
        int count = 0;         // Счетчик найденных допустимых исходов

        for (int j = 0; j < 128; j++) // Снова перебираем все 128 состояний (потенциальные исходы)
        {
            int m2 = 0, mx2 = 0, my2 = 0; // Инициализируем массу и импульс потенциального исхода
            for (int b = 0; b < 7; b++) { // Проверяем 7 битов в потенциальном исходе
                if (j & (1 << b)) {       // Если бит активен
                    m2++;
                    mx2 += px[b];         // Считаем импульс исхода по X
                    my2 += py[b];         // Считаем импульс исхода по Y
                }
            }
            // Если масса и импульс до и после совпадают - это допустимый исход столкновения:
            if (m2 == mass && mx2 == mx && my2 == my) // Строгая проверка законов сохранения
            {
                valid_states[count++] = j; // Сохраняем допустимый исход в массив и увеличиваем счетчик
            }
        }

        // Выбираем случайный исход, чтобы заставить газ перемешиваться:
        if (count > 0) // Если найден хотя бы один допустимый исход
        {
            int chosen = valid_states[rand() % count]; // Выбираем случайный вариант из допустимых
            
            // Если у столкновения есть варианты, заставляем частицы изменить направление:
            if (count > 1) // Если есть более 1 варианта (частицы могут отскочить)
            {
                do {
                    chosen = valid_states[rand() % count]; // Перевыбираем исход
                } while (chosen == i);                     // До тех пор, пока он не станет отличаться от начального (чтобы не пролетали насквозь)
            }
            col_table[0][i] = chosen; // Записываем выбранный исход в таблицу для газа
        } else 
        {
            col_table[0][i] = i; // Если вариантов нет, она летит дальше без изменений
        }
        col_table[1][i] = i; // Таблица для препятствий
    }
}

void init_grid(Cell **grid, int w, int h, float rho, int d) // Инициализация сетки вакуумом и камнем
{
    // Для стартового состояния игнорируем параметр rho, создавая вакуум:
    (void)rho; // Явно указываем компилятору, что переменная временно не используется
    
    double cx = w / 3.0; // Смещаем центр цилиндра влево на 1/3 экрана
    double cy = h / 2.0; // Позиция цилиндра строго по центру высоты
    double r2 = (d / 2.0) * (d / 2.0); // Вычисляем радиус в квадрате для формулы круга

    for (int y = 0; y < h; y++) // Проходим по высоте
    {
        for (int x = 0; x < w; x++) // Проходим по ширине
        {
            // Делаем клетку абсолютно пустой (вакуум):
            grid[y][x].state = 0;          // В клетке 0 частиц
            grid[y][x].is_obstacle = false; // По умолчанию клетка не твердая

            double dx_c = (double)x - cx; // Вычисляем дистанцию от клетки до центра круга по X
            double dy_c = (double)y - cy; // Вычисляем дистанцию от клетки до центра круга по Y
            
            // Устанавливаем цилиндр, газ не генерируем:
            if (dx_c * dx_c + dy_c * dy_c <= r2) // Формула принадлежности точки кругу
            {
                grid[y][x].is_obstacle = true; // Делаем клетку твердым препятствием
            }
        }
    }
}

void collide(Cell **grid, int w, int h) // Функция фазы микро-столкновений
{
    for (int y = 0; y < h; y++) // Проход по всем строкам
    {
        for (int x = 0; x < w; x++) // Проход по всем столбцам
        {
            // Внутри препятствия столкновений газа нет:
            if (grid[y][x].is_obstacle) // Если текущая клетка это камень
                continue;

            uint8_t current_state = grid[y][x].state; // Читаем текущее расположение частиц в клетке
            grid[y][x].state = col_table[0][current_state]; // Заменяем его на исход из ранее сгенерированной таблицы
        }
    }
}

void stream(Cell **src, Cell **dst, int w, int h, float rho_in) // Функция фазы переноса (движения)
{
    (void)rho_in; // Игнорируем параметр плотности

    // Очистка целевой решетки перед перемещением:
    for (int y = 0; y < h; y++) // Проход по Y
    {
        for (int x = 0; x < w; x++) // Проход по X
        {
            dst[y][x].state = 0; // Обнуляем частицы в целевой решетке
            dst[y][x].is_obstacle = src[y][x].is_obstacle; // Копируем информацию о том, где находится камень
        }
    }

    // Фаза перемещения:
    for (int y = 0; y < h; y++) // Проход по Y
    {
        for (int x = 0; x < w; x++) // Проход по X
        {
            if (src[y][x].is_obstacle) // Если клетка - камень
                continue; // Из препятствия ничего не вылетает, пропускаем.

            uint8_t st = src[y][x].state; // Запоминаем биты частиц в текущей клетке источника
            
            // Перенос частицы покоя:
            if (st & (1 << 6))               // Если есть 6-й бит (покоящаяся частица)
                dst[y][x].state |= (1 << 6); // Она остается в тех же координатах [y][x] целевой сетки

            int is_odd = y % 2; // Учет сдвига нечетных строк (гексагональная сетка зигзагом)

            for (int d = 0; d < 6; d++) // Перебираем 6 летящих направлений
            {
                if (st & (1 << d)) // Если частица летит в направлении 'd'
                {
                    int nx = x; // Подготавливаем новые координаты
                    int ny = y;

                    // Точный геометрический сдвиг координат для гексагональной сетки:
                    switch (d) // В зависимости от направления
                    {
                        case 0: nx++; break;                            // Восток
                        case 1: nx += is_odd; ny--; break;              // Северо-Восток
                        case 2: nx += is_odd - 1; ny--; break;          // Северо-Запад
                        case 3: nx--; break;                            // Запад
                        case 4: nx += is_odd - 1; ny++; break;          // Юго-Запад
                        case 5: nx += is_odd; ny++; break;              // Юго-Восток
                    }

                    // Граничное условие «Верх/Низ»: Зеркальное скольжение/отражение:
                    if (ny < 0 || ny >= h) // Если частица пытается вылететь за потолок или пол
                    {
                        int bounce_dir = (d + 3) % 6; // Считаем обратное направление (разворот на 180 градусов)
                        dst[y][x].state |= (1 << bounce_dir); // Возвращаем частицу обратно в свою клетку с новым вектором
                        continue; // Переходим к следующей частице
                    }

                    // Левая граница - поглощение вылетающих влево частиц:
                    if (nx < 0) // Если вылетает за левый край экрана
                        continue; // Просто уничтожаем ее

                    // Правая граница - свободный выход газа:
                    if (nx >= w) // Если вылетает за правый край экрана
                        continue; // Уничтожаем ее (вышла из трубы)

                    // Отскок от препятствия:
                    if (dst[ny][nx].is_obstacle) // Если клетка, куда она шагнула - это камень
                    {
                        int bounce_dir = (d + 3) % 6; // Разворот вектора обратно при ударе
                        dst[y][x].state |= (1 << bounce_dir);  // Частица не перемещается в nx, ny, а остается в y,x и летит назад
                    }
                    else
                    {
                        // Обычный бесконфликтный перенос в целевую ячейку:
                        dst[ny][nx].state |= (1 << d); // Записываем бит частицы в новые координаты
                    }
                }
            }
        }
    }

    for (int y = 0; y < h; y++) // Проходим по левому краю экрана
    {
        if (!dst[y][0].is_obstacle) // Если это не граница препятствия
        {
            // Принудительно ставим частицы, летящие вправо, вверх-вправо и вниз-вправо:
            dst[y][0].state |= (1 << 0) | (1 << 1) | (1 << 5);
        }
    }
}